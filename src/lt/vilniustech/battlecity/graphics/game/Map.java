    private List<Obstacle> obstacles = new ArrayList<>();
    private List<NpcTank> npcTanks = new ArrayList<>();
    private PlayerTank playerTank;
    private Home home;

    public static Map getInstance() {
        if (instance == null) {
            instance = new Map();
        }

        return instance;
    }

    public void init() {
        for (int y = 0; y < width(); y++) {
            for (int x = 0; x < height(); x++) {

                if (isMetalWall(x, y)) {
                    obstacles.add(
                            new MetalWall(x * MetalWall.LENGTH, y * MetalWall.WIDTH)
                    );
                }

                if (isCommonWall(x, y)) {
                    obstacles.add(
                            new CommonWall(x * CommonWall.LENGTH, y * CommonWall.WIDTH)
                    );
                }

                if (isHome(x, y)) {
                    home = new Home(x * Home.LENGTH, y * Home.WIDTH);
                }

                if (isPlayer(x, y)) {
                    playerTank = new PlayerTank(x * PlayerTank.LENGTH, y * PlayerTank.WIDTH);
                }
            }
        }
    }

    @Override
    public void draw(Graphics graphics) {
        for (Obstacle obstacle : obstacles) {
            obstacle.draw(graphics);
        }

        home.draw(graphics);
        playerTank.draw(graphics);
    }

    @Override
    public Rectangle getRectangle() {
        return null;
    }

    public boolean isMetalWall(int x, int y) {
        return model[y][x] == 1;
    }

    public boolean isCommonWall(int x, int y) {
        return model[y][x] == 2;
    }

    public boolean isRiver(int x, int y) {
        return model[y][x] == 3;
    }

    public boolean isTree(int x, int y) {
        return model[y][x] == 4;
    }

    public boolean isHome(int x, int y) {
        return model[y][x] == 6;
    }

    public boolean isPlayer(int x, int y) {
        return model[y][x] == 5;
    }

    public boolean isNpc(int x, int y) {
        return model[y][x] == 7;
    }

    public int width() {
        return model.length;
    }

    public int height() {
        return model[0].length;
    }

    public List<Obstacle> getObstacles() {
        return obstacles;
    }

    public List<NpcTank> getNpcTanks() {
        return npcTanks;
    }

    public PlayerTank getPlayerTank() {
        return playerTank;
    }

    public Home getHome() {
        return home;
    }
